package ucol.a1599116.tuckbox.util;

/**
 * Time helper utility
 * Used for everything time related
 */
public class TimeHelper {

    //Constants to hold time conversions to milliseconds
    private static final long SECOND_TO_MS = 1000;
    private static final long MINUTE_TO_MS = SECOND_TO_MS * 60;
    private static final long HOUR_TO_MS = MINUTE_TO_MS * 60;
    public static final long DAY_TO_MS = HOUR_TO_MS * 24;
    public static final long WEEK_TO_MS = DAY_TO_MS * 7;

}
